std::vector<double> get_midpoints(const std::vector<double>& int_vec) {
  std::vector<double> mid_vec;
  int size = int_vec.size() - 1;
  for (int i = 0; i != size; ++i) {
    mid_vec.push_back(0.5 * (int_vec[i] + int_vec[i+1]));
  }
  return mid_vec;
}

